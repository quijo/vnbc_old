            <p>October 7, 2016 – Students from Grade 1 to Grade 10 spent their first Friday of October by giving back to the community through a Clean Up Drive. The activity started around 8:00 a.m at the VNBC Chapel with Sir Leo Mahanlud, the Basic Education Principal and Boys and Girls Bridage Philippines (BGBP) Boys wing Vice President, giving instructions to the students as to where they would clean.
<ul>
<li>VNBC Grounds -  Grade 1-3</li>
<li>St. Mary’s Drive Road (VNBC Gate  to Country Mall) -  Grade 4-6</li>
<li>Camp Lapu-lapu Road – Grade 7-10</li>
</ul>
With the supervision of the teachers, the Clean Up Drive was a success. The garbage collected by the students was then turned into an Up cycled Project. Lanterns, Christmas tree holders, lamps, cellphone speakers and wall decors are just some of the things created from recycled materials such as bottles, plastic wrappers, straws and junked wheels.              

The event concluded with a reminder that some things, no matter how irrelevant, useless and broken they may seem, can still be transformed into new and beautiful masterpieces. Visayan Nazarene Bible College, in partnership with BGB, hopes that this activity would raise awareness and make people realize how big a problem improper waste disposal really is and encourage them to change their ways and start saving the nature by properly throwing and segregating garbage.

-By: Shandra Gywn B. Presbitero</p>


