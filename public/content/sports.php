<i><b>“Skill wins matches. Heart wins medals. The MILO Little Olympics is the grand nationwide tournament that witnesses the winning energy and winning attitude of the country's young champions.”</b></i> – MILO <br><br>

September 8-10, 2016 – Visayan Nazarene Bible College invades MILO Little Olympics 2016. 
The event started with the search for Miss Milo Little Olympics 2016 with contestants from two different categories – Elementary and High School.  
When asked how she was feeling prior to the competition, our very own Miss Milo Little Olympics representative Zarah Abellana, a Grade 9 student, expressed how she couldn’t contain her excitement and was ready to take on the stage.
With her demure personality and simple but sensational beauty, Ms. Abellana charms the crowd. She may not have won the crown but Ms. Abellana bagged something equally as much, a lesson in which she shared to us in a recent interview: <br><br>
<i><b>“The most important lesson I learned [from the competition] is that sometimes, it’s not just about winning, rather it’s opening oneself to various opportunities –that’s the greatest reward in a competition. To bring home a lot of fun memories and have new friends. ”</b></i><br><br>
The event was graced by the beauty of Ms. Gazini Christiana Ganados, one of the judges of the competition, and once reined as a Miss Milo Little Olympics on the year 2011. She is also a 2013 Miss World Finalist and the Miss Cesafi 2016 College Division grand winner, and we’re blessed to catch an exclusive interview with her.  <br><br>

